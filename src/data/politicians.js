// IDで政治家を取得
export function getPoliticianById(id) {
    return politicians.find(p => p.id === id) || null;
}

// 役職の重要度スコア（高いほど重要）
function getImportanceScore(politician) {
    const careers = politician.careers || [];
    if (careers.length === 0) return 0;

    const latestCareer = careers[0]?.title || '';

    // 代表・党首クラス
    if (latestCareer.includes('代表') || latestCareer.includes('党首') || latestCareer.includes('総裁')) return 100;
    // 幹事長・政調会長クラス
    if (latestCareer.includes('幹事長') || latestCareer.includes('政務調査会長') || latestCareer.includes('政調会長')) return 90;
    // 副代表・書記長クラス
    if (latestCareer.includes('副代表') || latestCareer.includes('書記長')) return 80;
    // 大臣クラス
    if (latestCareer.includes('大臣') && !latestCareer.includes('副大臣')) return 70;
    // 副大臣・委員長クラス
    if (latestCareer.includes('副大臣') || latestCareer.includes('委員長')) return 60;
    // その他役職
    if (latestCareer.includes('政務官') || latestCareer.includes('議長')) return 50;

    return 0;
}

// 首相経験があるかチェック
function hadPrimeMinisterExperience(politician) {
    const careers = politician.careers || [];
    return careers.some(c => c.title?.includes('内閣総理大臣') || c.title?.includes('総理大臣'));
}

// 政党で絞り込み（現在の所属＋元所属、重要度順にソート）
export function getPoliticiansByParty(party) {
    return politicians
        .filter(p => p.party === party || p.formerParty === party)
        .sort((a, b) => {
            // 優先度フィールドがあれば最優先
            const priorityA = a.priority || 0;
            const priorityB = b.priority || 0;
            if (priorityA !== priorityB) return priorityB - priorityA;

            // 首相経験者を優先
            const pmA = hadPrimeMinisterExperience(a) ? 200 : 0;
            const pmB = hadPrimeMinisterExperience(b) ? 200 : 0;
            if (pmA !== pmB) return pmB - pmA;

            // 重要度スコアで比較
            return getImportanceScore(b) - getImportanceScore(a);
        });
}
